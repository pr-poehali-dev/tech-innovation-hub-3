export function exportToPdf() {
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' })

  const pageW = doc.internal.pageSize.getWidth()
  const pageH = doc.internal.pageSize.getHeight()
  const margin = 20
  const maxW = pageW - margin * 2
  let y = margin

  const checkPage = (needed: number) => {
    if (y + needed > pageH - margin) {
      doc.addPage()
      y = margin
    }
  }

  // Title
  doc.setFont('helvetica', 'bold')
  doc.setFontSize(18)
  const titleLines = doc.splitTextToSize('Судебные прения в гражданском процессе', maxW)
  doc.text(titleLines, margin, y)
  y += titleLines.length * 8 + 4

  doc.setFontSize(10)
  doc.setFont('helvetica', 'normal')
  doc.setTextColor(120, 120, 120)
  doc.text('Материал подготовлен на основании ГПК РФ, ст. 190', margin, y)
  y += 10
  doc.setTextColor(0, 0, 0)

  // Divider
  doc.setDrawColor(200, 200, 200)
  doc.line(margin, y, pageW - margin, y)
  y += 8

  for (const section of content) {
    checkPage(16)

    // Section title
    doc.setFont('helvetica', 'bold')
    doc.setFontSize(13)
    const sectionLines = doc.splitTextToSize(section.title, maxW)
    doc.text(sectionLines, margin, y)
    y += sectionLines.length * 6 + 4

    // Paragraphs
    doc.setFont('helvetica', 'normal')
    doc.setFontSize(10)

    for (const para of section.paragraphs) {
      const lines = doc.splitTextToSize(para, maxW)
      checkPage(lines.length * 5 + 3)
      doc.text(lines, margin, y)
      y += lines.length * 5 + 3
    }

    y += 6
  }

  // Footer on each page
  const totalPages = doc.getNumberOfPages()
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i)
    doc.setFontSize(8)
    doc.setTextColor(150, 150, 150)
    doc.text(`Страница ${i} из ${totalPages}`, pageW - margin, pageH - 10, { align: 'right' })
    doc.text('Судебные прения · ГПК РФ ст. 190', margin, pageH - 10)
  }

  doc.save('sudebnie-preniya.pdf')
}
